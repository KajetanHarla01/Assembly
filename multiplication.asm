    LJMP START
	ORG	100H
START:
	LCALL LCD_INIT
	LCALL WAIT_KEY
    MOV B, #16
    MUL AB
	MOV R1, A
	LCALL WAIT_KEY
    ADD A, R1
    MOV R1, A
    LCALL WAIT_KEY
    MOV B, #16
    MUL AB
	MOV R2, A
	LCALL WAIT_KEY
    ADD A, R2
    MOV R2, A
	MOV A, R1
    MOV B, R2
	MUL AB
    MOV R1, A
    MOV A, B
	LCALL WRITE_HEX
    MOV A, R1
    LCALL WRITE_HEX
STOP:
	LJMP STOP
	NOP